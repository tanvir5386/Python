# এটি একটি নাম্বার গেসিং গেম। এখানে প্রোগ্রামটি ১-১০০ এর মধ্যে একটি রেন্ডম নাম্বার জেনারেট করে।
# প্লেয়ার কে সেই নাম্বারটি অনুমান করতে হয়। প্রতিবার ভুল অনুমান করলে প্রোগ্রাম জানায় নাম্বারটি বেশি নাকি কম।
# সঠিক অনুমান করলে গেম শেষ হয়।
import random

# রেন্ডম নাম্বার জেনারেট
secret = random.randint(1, 100)
guess = 0

# মূল গেম লুপ
while guess != secret:
    # ইউজার ইনপুট নেওয়া
    guess = int(input("একটি নাম্বার অনুমান করুন (১-১০০): "))
    
    # অনুমান চেক করা
    if guess < secret:
        print("আপনার অনুমান খুব ছোট!")
    elif guess > secret:
        print("আপনার অনুমান খুব বড়!")

print("অভিনন্দন! আপনি সঠিক অনুমান করেছেন!")
